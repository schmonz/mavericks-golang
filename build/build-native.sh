#!/bin/sh
set -eu
here="$(cd "$(dirname "$0")" && pwd)"
. "$here/versions.sh"
command -v go >/dev/null || { echo "FATAL: need a stock bootstrap go on PATH" >&2; exit 1; }
GOROOT_BOOTSTRAP=$(go env GOROOT); export GOROOT_BOOTSTRAP

# 1. inputs. Capture the last stdout line of the noisy builders, then assert each
# result is real -- a piped $(... | tail -1) hides upstream failure (tail exits 0),
# so without these guards a failed sub-step would feed an empty/garbage value on.
: "${MSC_SCRIPTS:?mavericks-shared-cmake not found; install it (cmake --install --prefix \$HOME/.local) -- see its README}"
SDK=$(sh "$MSC_SCRIPTS/fetch_sdk.sh"); export SDK
[ -d "$SDK" ] || { echo "FATAL: 10.9 SDK not found: '$SDK'" >&2; exit 1; }
sh "$here/fetch-go.sh"
sh "$here/apply-patches.sh"
LEGACY_A=$(sh "$here/build-legacy-support.sh" | tail -1); export LEGACY_A
[ -f "$LEGACY_A" ] || { echo "FATAL: legacy-support .a not built: '$LEGACY_A'" >&2; exit 1; }
CA=$(sh "$here/fetch-ca.sh" | tail -1)
[ -f "$CA" ] || { echo "FATAL: CA bundle not produced: '$CA'" >&2; exit 1; }

# 2. link recipe
. "$here/link-recipe.sh"
# Cross build: the bootstrap-phase tools (cmd/dist and bootstrap/cmd/...) are
# compiled by the bootstrap Go for ITS native arch (arm64 on this host) and run
# on the modern host, so they must NOT receive the x86_64/min-10.9 legacy flags.
# GO_BOOTSTRAP_LDFLAGS drives exactly those host-arch link paths (patches
# 0001/0002); unset it here. The target amd64/10.9 toolchain still gets legacy
# support via GO_LDFLAGS / BOOT_GO_LDFLAGS / CGO_LDFLAGS. (Native/M5 keeps it.)
unset GO_BOOTSTRAP_LDFLAGS

# 3. cross-build: amd64/min-10.9 toolchain (amd64 tools via Rosetta)
export GOOS=darwin GOARCH=amd64 GOHOSTOS=darwin GOHOSTARCH=amd64
export CGO_ENABLED=1 CC=/usr/bin/clang
export GOROOT_FINAL="$PREFIX"
export GOCACHE="$WORK/.gocache"
( cd "$WORK/go/src" && ./make.bash -v )

# 4. stage into a DESTDIR tree mirroring the install prefix, + CA bundle
stage="$WORK/staging"
rm -rf "$stage"; mkdir -p "$stage$PREFIX"
( cd "$WORK/go" && rm -rf pkg/obj pkg/bootstrap && pax -rw . "$stage$PREFIX" )
mkdir -p "$stage$PREFIX/etc/openssl/certs"
cp "$CA" "$stage$PREFIX/etc/openssl/certs/ca-certificates.crt"

# 5. downstream-link defaults. Modern Go ALWAYS external-links on darwin, so every
# binary built on the 10.9 box (cgo, pure-Go, or transitive-cgo) needs the legacy
# shim + the -Wl,-U set at link time, or on-box clang can't resolve _clock_gettime
# / the *at family / notify_* on 10.9. CGO_LDFLAGS alone only reaches the link when
# main itself is cgo, so instead ship an internal CC wrapper and make it the default
# CC in $GOROOT/go.env. It is invisible to users -- a plain `go build` just works.
# The static .a lives in the prefix; the wrapper appends it + the -Wl,-U allowances
# on link steps only (compiles pass through). Absolute paths are stable: the
# toolchain always installs at $PREFIX.
mkdir -p "$stage$PREFIX/lib"
cp "$LEGACY_A" "$stage$PREFIX/lib/libMacportsLegacySupport.a"

_wlu=""; for s in $WLU_SYMS; do _wlu="$_wlu \\
  -Wl,-U,$s"; done
cat > "$stage$PREFIX/bin/mavericks-clang" <<EOF
#!/bin/sh
# Internal Mavericks downstream-link wrapper (default CC via go.env). Appends the
# static legacy shim + the -Wl,-U allowances on LINK steps so every 10.9 binary
# resolves clock_gettime, the *at family, and notify/xpc. Compile-only steps
# (-c/-E/-S) pass through untouched. Generated by build-native.sh.
for a in "\$@"; do
  case "\$a" in -c|-E|-S) exec /usr/bin/clang "\$@" ;; esac
done
exec /usr/bin/clang "\$@" \\
  "$PREFIX/lib/libMacportsLegacySupport.a"$_wlu
EOF
chmod 755 "$stage$PREFIX/bin/mavericks-clang"

{
  echo "CGO_ENABLED=1"
  echo "CC=$PREFIX/bin/mavericks-clang"
} >> "$stage$PREFIX/go.env"

test -x "$stage$PREFIX/bin/go" || { echo "FATAL: no bin/go staged" >&2; exit 1; }
echo "$stage$PREFIX"
